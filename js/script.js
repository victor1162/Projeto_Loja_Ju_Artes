function abrirCatalogo1(){
	let uls1 = document.getElementById('drop1')
	let areaDropdown1 = document.querySelector('.catalogo-dropdown')
	
	if(areaDropdown1.style.height === '0px'){

		areaDropdown1.style.height = '146px'
			uls1.style.opacity = '1'
			uls1.style.display = 'block'
			areaDropdown1.style.transition = '1s'
			areaDropdown1.style.background = '#FF6BB5'
			uls1.style.color = ''

	}else{
		areaDropdown1.style.height = '0px'
		uls1.style.opacity = '0'
		uls1.style.display = 'none'
	}
}

